using System;
using Xunit;
using Training20200810.Questions;
using System.Collections.Generic;
using System.Linq;
using Training20200810.Collections;

namespace Training20200810.Test
{
    public class AtCoderTester
    {
        [Theory]
        [InlineData(@"4
2 3 5
2 1 5
1 2 5
3 2 5", @"2 2 6")]
        [InlineData(@"2
0 0 100
1 1 98", @"0 0 100")]
        [InlineData(@"3
99 1 191
100 1 192
99 0 192", @"100 0 193")]
        public void QuestionATest(string input, string output)
        {
            var outputs = SplitByNewLine(output);
            IAtCoderQuestion question = new QuestionA();

            var answers = question.Solve(input).Select(o => o.ToString()).ToArray();

            Assert.Equal(outputs, answers);
        }

        [Theory]
        [InlineData(@"2 3 1", @"1.0000000000")]
        [InlineData(@"1000000000 180707 0", @"0.0001807060")]
        public void QuestionBTest(string input, string output)
        {
            var outputs = SplitByNewLine(output);
            IAtCoderQuestion question = new QuestionB();

            var answers = question.Solve(input).Select(o => o.ToString()).ToArray();

            AssertNearlyEqual(outputs, answers);
        }

        //[Theory]
        //[InlineData(@"", @"")]
        public void QuestionCTest(string input, string output)
        {
            var outputs = SplitByNewLine(output);
