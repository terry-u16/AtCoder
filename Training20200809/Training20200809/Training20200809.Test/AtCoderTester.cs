    public class AtCoderTester
    {
        [Theory]
        [InlineData(@"4 3
-1 0 3
0 0 3
1 0 2
1 1 40", @"2.4")]
        [InlineData(@"10 5
-879 981 26
890 -406 81
512 859 97
362 -955 25
128 553 17
-885 763 2
449 310 57
-656 -204 11
-270 76 40
184 170 16", @"7411.2252")]
        public void QuestionATest(string input, string output)
        {
            var outputs = SplitByNewLine(output);
            IAtCoderQuestion question = new QuestionA();

            var answers = question.Solve(input).Select(o => o.ToString()).ToArray();

            AssertNearlyEqual(outputs, answers);
        }

        [Theory]
        [InlineData(@"atcoderregularcontest", @"b")]
        [InlineData(@"abcdefghijklmnopqrstuvwxyz", @"None")]
        [InlineData(@"fajsonlslfepbjtsaayxbymeskptcumtwrmkkinjxnnucagfrg", @"d")]
